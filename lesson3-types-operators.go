package main

import "fmt"

func main() {
  fmt.Println("Numbers: ")
  fmt.Println("Addition ")
  fmt.Println("1 + 1 =", 1 + 1)

  fmt.Println("Subtraction ")
  fmt.Println("2 - 1 =", 2 - 1)

  fmt.Println("Multiplication ")
  fmt.Println("3 * 3 =", 3 * 3)

  fmt.Println("Division (float)")
  fmt.Println("4 / 3 =", 4.0 / 3.0)

  fmt.Println("Division (int)")
  fmt.Println("4 / 3 =", 4 / 3)

  fmt.Println("Remainder ")
  fmt.Println("3 % 2 =", 3 % 2)
}